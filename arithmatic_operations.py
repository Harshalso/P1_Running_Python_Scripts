print("The multiplication of the {} and {} is {}".format( num_1, num_2, multiplication ))

division = num_1 / num_2
print("The division of the {} and {} is {}".format( num_1, num_2, division))
modulo = num_1 % num_2
print("The modulo of the {} and {} is {}".format( num_1, num_2, modulo))
